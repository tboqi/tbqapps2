package com.cc.cw.pic.module;

public class Huace {

	private String huaceId;

	private String huaceName;

	private String huaceInfo;

	public String getHuaceId() {
		return huaceId;
	}

	public void setHuaceId(String huaceId) {
		this.huaceId = huaceId;
	}

	public String getHuaceInfo() {
		return huaceInfo;
	}

	public void setHuaceInfo(String huaceInfo) {
		this.huaceInfo = huaceInfo;
	}

	public String getHuaceName() {
		return huaceName;
	}

	public void setHuaceName(String huaceName) {
		this.huaceName = huaceName;
	}
}
